export type TKey = keyof typeof dict.en;

type Ctx = {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: TKey, vars?: Record<string, string>) => string;
  dir: "ltr" | "rtl";
};

const LangContext = createContext<Ctx | null>(null);

const STORAGE_KEY = "nexus.lang";

function interpolate(template: string, vars?: Record<string, string>) {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, k: string) => vars[k] ?? `{${k}}`);
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");

  // Hydrate from storage after mount to avoid SSR mismatch.
  useEffect(() => {
    try {
      const stored = window.localStorage.getItem(STORAGE_KEY) as Lang | null;
      if (stored === "en" || stored === "ar") setLangState(stored);
    } catch {
      /* storage may be blocked; ignore */
    }
  }, []);

  // Reflect language + direction onto the root <html> element.
  useEffect(() => {
    const html = document.documentElement;
    html.lang = lang;
    html.dir = LANG_LABELS[lang].dir;
  }, [lang]);

  const value = useMemo<Ctx>(
    () => ({
      lang,
      dir: LANG_LABELS[lang].dir,
      setLang: (l) => {
        setLangState(l);
        try {
          window.localStorage.setItem(STORAGE_KEY, l);
        } catch {
          /* ignore */
        }
      },
      t: (key, vars) => interpolate(dict[lang][key] ?? dict.en[key] ?? key, vars),
    }),
    [lang],
  );

  return <LangContext.Provider value={value}>{children}</LangContext.Provider>;
}

export function useT(): Ctx {
  const ctx = useContext(LangContext);
  if (!ctx) throw new Error("useT must be used within <LanguageProvider>");
  return ctx;
}
